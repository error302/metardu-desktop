function classifyTraverseAccuracy(precision: number): string {
  if (precision >= 10000) return 'First Order Class I — RDM 1.1 Table 5.2'
  if (precision >= 5000) return 'First Order Class II — RDM 1.1 Table 5.2'
  if (precision >= 3000) return 'Second Order Class I — RDM 1.1 Table 5.2'
  if (precision >= 1000) return 'Second Order Class II — RDM 1.1 Table 5.2'
  return 'Third Order — RDM 1.1 Table 5.2 (WARNING: below standard)'
}

