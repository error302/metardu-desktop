const SECTION_TITLES: Record<ReportSection, { number: number; title: string }> = {
  TITLE_PAGE: { number: 1, title: 'Title Page' },
  TABLE_OF_CONTENTS: { number: 2, title: 'Table of Contents' },
  INTRODUCTION: { number: 3, title: 'Introduction' },
  SCOPE_OF_WORK: { number: 4, title: 'Scope of Work' },
  EQUIPMENT_AND_PERSONNEL: { number: 5, title: 'Equipment and Personnel' },
  CONTROL_SURVEY: { number: 6, title: 'Control Survey' },
  TOPOGRAPHIC_SURVEY: { number: 7, title: 'Topographic Survey' },
  DATA_PROCESSING: { number: 8, title: 'Data Processing' },
  RESULTS_AND_ACCURACY: { number: 9, title: 'Results and Accuracy' },
  COORDINATE_REGISTER: { number: 10, title: 'Coordinate Register' },
  BENCHMARK_REGISTER: { number: 11, title: 'Benchmark Register' },
  TRAVERSE_COMPUTATIONS: { number: 12, title: 'Traverse Computations' },
  LEVELLING_COMPUTATIONS: { number: 13, title: 'Levelling Computations' },
  CONCLUSIONS_AND_RECOMMENDATIONS: { number: 14, title: 'Conclusions and Recommendations' },
  DECLARATION: { number: 15, title: 'Declaration' }
}

function createSection(
  section: ReportSection,
  content: string,
  autoGenerated: boolean
): SectionContent {
  const info = SECTION_TITLES[section]
  const completeness = autoGenerated ? 100 : calculateCompleteness(content)
  return {
    section,
    sectionNumber: info.number,
    title: info.title,
    content,
    autoGenerated,
    completeness
  }
}

function calculateCompleteness(content: string): number {
  const length = content.length
  if (length === 0) return 0
  if (length < 100) return 30
  if (length < 300) return 60
  if (length < 500) return 80
  return 90
}

