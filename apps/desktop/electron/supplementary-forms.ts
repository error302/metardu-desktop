export interface LevelingReading {
  station: string;              // staff station label
  backsight?: number;           // staff reading (m), if a BS was taken
  foresight?: number;           // staff reading (m), if a FS was taken
  intermediateSight?: number;   // staff reading (m), if IS
  changePoint?: boolean;        // mark as change point
  distance?: number;            // m, distance to staff (for closure calc)
  remarks?: string;
}

export interface LevelingPage {
  pageNumber: number;
  benchmarkStart: { name: string; elevation: number };  // m
  benchmarkEnd?: { name: string; elevation: number };     // m (for closing run)
  readings: LevelingReading[];
  // Computed:
  pageCheck?: {
    sumBS: number;
    sumFS: number;
    sumRise: number;
    sumFall: number;
    firstRL: number;
    lastRL: number;
    bsMinusFs: number;
    riseMinusFall: number;
    lastMinusFirst: number;
    pageCheckPass: boolean;
  };
}

export interface LevelingBookInput {
  project: CommonProject & {
    roadName?: string;
    roadClass?: string;
    levelType: string;          // e.g. "Automatic Level (Leica NA2)", "Digital Level (Trimble DiNi)"
    levelSerial: string;
    staffType: string;           // e.g. "Fiberglass 5m", "Barcoded 3m"
    staffSerial: string;
    closureStandard: string;    // e.g. "10*sqrt(K) mm (RDM 1.1)"
  };
  surveyor: CommonSurveyor;
  pages: LevelingPage[];
  // Closure results:
  closure?: {
    totalDistance: number;       // m
    numberOfSetups: number;
    misclosure: number;          // mm
    allowable: number;           // mm
    passes: boolean;
    precisionClass: string;      // e.g. "Class I (Engineering) — 1st order"
  };
  outputPath: string;
  sealWithRSA?: boolean;
  keypair?: SurveyorKeypair;
}

export async function generateLevelingBook(input: LevelingBookInput): Promise<FormResult> {
  log.info(`Generating Leveling Book (${input.pages.length} pages)`);

  const warnings: string[] = [];
  if (input.pages.length === 0) {
    warnings.push('No leveling pages provided.');
  }
  // Compute page checks if not provided
  for (let i = 0; i < input.pages.length; i++) {
    const p = input.pages[i];
    let sumBS = 0, sumFS = 0, sumRise = 0, sumFall = 0;
    let prevReading: number | null = null;
    let firstRL = p.benchmarkStart.elevation;
    let currentRL = firstRL;
    let lastRL = firstRL;

    for (const r of p.readings) {
      if (r.backsight != null) {
        sumBS += r.backsight;
        if (prevReading != null) {
          // rise/fall from previous FS to this BS
          const diff = prevReading - r.backsight;
          if (diff >= 0) sumRise += diff; else sumFall += Math.abs(diff);
          currentRL += diff;
        }
      }
      if (r.foresight != null) {
        sumFS += r.foresight;
        if (prevReading != null) {
          // rise/fall from previous BS to this FS
          const diff = prevReading - r.foresight;
          if (diff >= 0) sumRise += diff; else sumFall += Math.abs(diff);
          currentRL += diff;
        }
        prevReading = r.foresight;
        lastRL = currentRL;
      } else if (r.backsight != null) {
        prevReading = r.backsight;
      }
    }

    const bsMinusFs = sumBS - sumFS;
    const riseMinusFall = sumRise - sumFall;
    const lastMinusFirst = lastRL - firstRL;
    const pageCheckPass = Math.abs(bsMinusFs - riseMinusFall) < 0.001 && Math.abs(riseMinusFall - lastMinusFirst) < 0.001;

    if (!p.pageCheck) {
      p.pageCheck = { sumBS, sumFS, sumRise, sumFall, firstRL, lastRL, bsMinusFs, riseMinusFall, lastMinusFirst, pageCheckPass };
    }
    if (!pageCheckPass) {
      warnings.push(`Page ${p.pageNumber}: page check failed (BS-FS=${bsMinusFs.toFixed(3)}, rise-fall=${riseMinusFall.toFixed(3)}, last-first=${lastMinusFirst.toFixed(3)}).`);
    }
  }

  // Closure check
  if (input.closure) {
    const c = input.closure;
    if (!c.passes) {
      warnings.push(`Run closure failed: ${c.misclosure.toFixed(1)}mm > ${c.allowable.toFixed(1)}mm allowable.`);
    }
  }

  const doc = new PDFDocument({
    size: [A4_WIDTH_PT, A4_HEIGHT_PT],
    margins: { top: MARGIN_PT, bottom: MARGIN_PT, left: MARGIN_PT, right: MARGIN_PT },
    info: {
      Title: `Leveling Book — ${input.project.parcelNumber}`,
      Author: `${input.surveyor.name} (Lic. ${input.surveyor.license})`,
      Subject: 'Leveling Book (Rise and Fall Method)',
      Keywords: 'RDM 1.1, Leveling, METARDU Desktop',
      Producer: 'METARDU Desktop',
    },
  });

  const stream = fs.createWriteStream(input.outputPath);
  doc.pipe(stream);

  // ─── Page 1: Cover + Equipment + Closure Summary ─────────────────
  drawHeader(doc, input.project, '1', 'LEVELING BOOK — RISE AND FALL METHOD');

  let y = MARGIN_PT + 30;
  doc.font('Helvetica-Bold').fontSize(FS_TITLE + 2).fillColor(COLOR_ACCENT)
    .text('LEVELING BOOK', MARGIN_PT, y, { align: 'center', width: A4_WIDTH_PT - 2 * MARGIN_PT });
  y += 22;
  doc.font('Helvetica-Bold').fontSize(FS_HEADING).fillColor(COLOR_BLACK)
    .text('RISE AND FALL METHOD', MARGIN_PT, y, { align: 'center', width: A4_WIDTH_PT - 2 * MARGIN_PT });
  y += 18;
  doc.font('Helvetica-Oblique').fontSize(FS_SUBHEADING).fillColor(COLOR_GREY)
    .text('Per Road Design Manual RDM 1.1 (2025), Section 5',
      MARGIN_PT, y, { align: 'center', width: A4_WIDTH_PT - 2 * MARGIN_PT });

  y += 22;
  const labelW = 50 * MM_TO_PT;
  const valW = (A4_WIDTH_PT - 2 * MARGIN_PT) - labelW;
  const projectRows: Array<[string, string]> = [
    ['Project Name', input.project.name],
    ['Parcel Number', input.project.parcelNumber],
    ['LR Number', input.project.lrNumber],
    ['County', input.project.county],
    ['Locality', input.project.locality],
    ['Road Name', input.project.roadName ?? '—'],
    ['Road Class', input.project.roadClass ?? '—'],
    ['Survey Date', input.project.surveyDate],
    ['Surveyor', `${input.surveyor.name} (Lic. ${input.surveyor.license})`],
    ['Surveyor Firm', input.surveyor.firmName ?? '—'],
    ['Level Type', input.project.levelType],
    ['Level Serial', input.project.levelSerial],
    ['Staff Type', input.project.staffType],
    ['Staff Serial', input.project.staffSerial],
    ['Closure Standard', input.project.closureStandard],
    ['Total Pages', String(input.pages.length)],
  ];
  for (const [label, value] of projectRows) {
    doc.font('Helvetica-Bold').fontSize(FS_BODY).fillColor(COLOR_BLACK)
      .text(label, MARGIN_PT, y, { width: labelW });
    doc.font('Helvetica').fontSize(FS_BODY).fillColor(COLOR_BLACK)
      .text(value, MARGIN_PT + labelW, y, { width: valW });
    doc.moveTo(MARGIN_PT, y + 12).lineTo(A4_WIDTH_PT - MARGIN_PT, y + 12).lineWidth(0.2).stroke(COLOR_LIGHT_GREY);
    y += 14;
  }

  // Closure summary
  if (input.closure) {
    y += 12;
    doc.font('Helvetica-Bold').fontSize(FS_HEADING).fillColor(COLOR_ACCENT)
      .text('RUN CLOSURE SUMMARY', MARGIN_PT, y);
    y += 16;

    const c = input.closure;
    const closureRows: Array<[string, string, string]> = [
      ['Total Distance', `${fmtDistance(c.totalDistance)} m`, ''],
      ['Number of Setups', String(c.numberOfSetups), ''],
      ['Misclosure', `${c.misclosure.toFixed(2)} mm`, ''],
      ['Allowable', `${c.allowable.toFixed(2)} mm`, c.closureStandard],
      ['Result', c.passes ? 'PASS' : 'FAIL', c.passes ? 'OK' : 'CHECK'],
      ['Precision Class', c.precisionClass, ''],
    ];
    for (const [label, value, status] of closureRows) {
      doc.font('Helvetica-Bold').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(label, MARGIN_PT, y, { width: labelW });
      doc.font('Courier').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(value, MARGIN_PT + labelW, y, { width: valW });
      doc.font('Helvetica-Bold').fontSize(FS_BODY)
        .fillColor(status === 'OK' || status === 'PASS' ? COLOR_BLACK : status === 'CHECK' || status === 'FAIL' ? COLOR_WARNING : COLOR_GREY)
        .text(status, MARGIN_PT + labelW + valW, y, { width: 30 * MM_TO_PT });
      doc.moveTo(MARGIN_PT, y + 12).lineTo(A4_WIDTH_PT - MARGIN_PT, y + 12).lineWidth(0.2).stroke(COLOR_LIGHT_GREY);
      y += 14;
    }
  }

  drawFooter(doc, input.project, input.surveyor);

  // ─── Pages 2+: One per leveling page ─────────────────────────────
  for (let i = 0; i < input.pages.length; i++) {
    const p = input.pages[i];
    doc.addPage();
    drawHeader(doc, input.project, String(i + 2), `LEVELING PAGE ${p.pageNumber}`);
    y = MARGIN_PT + 30;

    doc.font('Helvetica-Bold').fontSize(FS_TITLE).fillColor(COLOR_ACCENT)
      .text(`PAGE ${p.pageNumber}`, MARGIN_PT, y, { align: 'center', width: A4_WIDTH_PT - 2 * MARGIN_PT });
    y += 18;

    // Bench mark info
    doc.font('Helvetica-Bold').fontSize(FS_BODY).fillColor(COLOR_BLACK)
      .text(`Starting BM: ${p.benchmarkStart.name} (RL ${p.benchmarkStart.elevation.toFixed(3)} m)`, MARGIN_PT, y, { width: 80 * MM_TO_PT });
    if (p.benchmarkEnd) {
      doc.font('Helvetica-Bold').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(`Closing BM: ${p.benchmarkEnd.name} (RL ${p.benchmarkEnd.elevation.toFixed(3)} m)`, MARGIN_PT + 80 * MM_TO_PT, y, { width: 80 * MM_TO_PT });
    }
    y += 16;

    // Readings table header
    const cols = [
      { title: 'Stn', x: MARGIN_PT, w: 18 * MM_TO_PT, align: 'left' as const },
      { title: 'BS (m)', x: MARGIN_PT + 18 * MM_TO_PT, w: 18 * MM_TO_PT, align: 'right' as const },
      { title: 'IS (m)', x: MARGIN_PT + 36 * MM_TO_PT, w: 18 * MM_TO_PT, align: 'right' as const },
      { title: 'FS (m)', x: MARGIN_PT + 54 * MM_TO_PT, w: 18 * MM_TO_PT, align: 'right' as const },
      { title: 'Rise', x: MARGIN_PT + 72 * MM_TO_PT, w: 18 * MM_TO_PT, align: 'right' as const },
      { title: 'Fall', x: MARGIN_PT + 90 * MM_TO_PT, w: 18 * MM_TO_PT, align: 'right' as const },
      { title: 'RL (m)', x: MARGIN_PT + 108 * MM_TO_PT, w: 24 * MM_TO_PT, align: 'right' as const },
      { title: 'Dist (m)', x: MARGIN_PT + 132 * MM_TO_PT, w: 22 * MM_TO_PT, align: 'right' as const },
      { title: 'Remarks', x: MARGIN_PT + 154 * MM_TO_PT, w: 41 * MM_TO_PT, align: 'left' as const },
    ];
    for (const c of cols) {
      doc.font('Helvetica-Bold').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(c.title, c.x, y, { width: c.w, align: c.align });
    }
    y += 12;
    doc.moveTo(MARGIN_PT, y).lineTo(A4_WIDTH_PT - MARGIN_PT, y).lineWidth(0.5).stroke(COLOR_BLACK);

    // Readings rows (with rise/fall computation)
    let prevReading: number | null = null;
    let currentRL = p.benchmarkStart.elevation;

    for (const r of p.readings) {
      y += 2;
      let rise = 0, fall = 0;
      const reading = r.foresight ?? r.backsight ?? r.intermediateSight ?? 0;

      if (r.foresight != null && prevReading != null) {
        const diff = prevReading - r.foresight;
        if (diff >= 0) rise = diff; else fall = Math.abs(diff);
        currentRL += diff;
      } else if (r.backsight != null && prevReading != null) {
        const diff = prevReading - r.backsight;
        if (diff >= 0) rise = diff; else fall = Math.abs(diff);
        currentRL += diff;
      }

      // Row
      doc.font('Helvetica-Bold').fontSize(FS_SMALL).fillColor(COLOR_BLACK)
        .text(r.station, cols[0].x, y, { width: cols[0].w, align: 'left' });
      doc.font('Courier').fontSize(FS_SMALL).fillColor(COLOR_BLACK)
        .text(r.backsight != null ? r.backsight.toFixed(3) : '', cols[1].x, y, { width: cols[1].w, align: 'right' })
        .text(r.intermediateSight != null ? r.intermediateSight.toFixed(3) : '', cols[2].x, y, { width: cols[2].w, align: 'right' })
        .text(r.foresight != null ? r.foresight.toFixed(3) : '', cols[3].x, y, { width: cols[3].w, align: 'right' })
        .text(rise > 0 ? rise.toFixed(3) : '', cols[4].x, y, { width: cols[4].w, align: 'right' })
        .text(fall > 0 ? fall.toFixed(3) : '', cols[5].x, y, { width: cols[5].w, align: 'right' })
        .text(currentRL.toFixed(3), cols[6].x, y, { width: cols[6].w, align: 'right' })
        .text(r.distance != null ? r.distance.toFixed(1) : '', cols[7].x, y, { width: cols[7].w, align: 'right' });
      doc.font('Helvetica').fontSize(FS_SMALL).fillColor(COLOR_GREY)
        .text(r.remarks ?? '', cols[8].x, y, { width: cols[8].w, align: 'left' });
      y += 11;
      doc.moveTo(MARGIN_PT, y).lineTo(A4_WIDTH_PT - MARGIN_PT, y).lineWidth(0.1).stroke(COLOR_LIGHT_GREY);

      // Update prevReading for next iteration
      if (r.foresight != null) prevReading = r.foresight;
      else if (r.backsight != null) prevReading = r.backsight;

      // Page break if needed
      if (y > A4_HEIGHT_PT - MARGIN_PT - 50) {
        // Add continuation page
        doc.addPage();
        drawHeader(doc, input.project, `${i + 2} (cont.)`, `LEVELING PAGE ${p.pageNumber} (cont.)`);
        y = MARGIN_PT + 30;
        for (const c of cols) {
          doc.font('Helvetica-Bold').fontSize(FS_BODY).fillColor(COLOR_BLACK)
            .text(c.title, c.x, y, { width: c.w, align: c.align });
        }
        y += 12;
        doc.moveTo(MARGIN_PT, y).lineTo(A4_WIDTH_PT - MARGIN_PT, y).lineWidth(0.5).stroke(COLOR_BLACK);
      }
    }

    // Page check
    if (p.pageCheck) {
      const pc = p.pageCheck;
      y += 8;
      doc.font('Helvetica-Bold').fontSize(FS_HEADING).fillColor(COLOR_ACCENT)
        .text('PAGE CHECK', MARGIN_PT, y);
      y += 14;

      const checkRows: Array<[string, string, string, string]> = [
        ['Sum BS', pc.sumBS.toFixed(3), 'Sum FS', pc.sumFS.toFixed(3)],
        ['Sum Rise', pc.sumRise.toFixed(3), 'Sum Fall', pc.sumFall.toFixed(3)],
        ['BS - FS', pc.bsMinusFs.toFixed(3), 'Rise - Fall', pc.riseMinusFall.toFixed(3)],
        ['First RL', pc.firstRL.toFixed(3), 'Last RL', pc.lastRL.toFixed(3)],
        ['Last - First', pc.lastMinusFirst.toFixed(3), 'Page Check', pc.pageCheckPass ? 'PASS' : 'FAIL'],
      ];
      const colW = 35 * MM_TO_PT;
      for (const [l1, v1, l2, v2] of checkRows) {
        doc.font('Helvetica-Bold').fontSize(FS_SMALL).fillColor(COLOR_BLACK)
          .text(l1, MARGIN_PT, y, { width: colW })
          .text(l2, MARGIN_PT + 2 * colW, y, { width: colW });
        doc.font('Courier').fontSize(FS_SMALL).fillColor(COLOR_BLACK)
          .text(v1, MARGIN_PT + colW, y, { width: colW, align: 'right' });
        doc.font('Courier').fontSize(FS_SMALL)
          .fillColor(l2 === 'Page Check' && !pc.pageCheckPass ? COLOR_WARNING : COLOR_BLACK)
          .text(v2, MARGIN_PT + 3 * colW, y, { width: colW, align: 'right' });
        y += 11;
      }
    }

    drawFooter(doc, input.project, input.surveyor);
  }

  doc.end();
  await new Promise<void>((resolve, reject) => {
    stream.on('finish', () => resolve());
    stream.on('error', reject);
  });

  const sealResult = await sealAndAppendCertificate(
    input.outputPath,
    input.surveyor,
    input.project,
    (certDoc, _seal) => drawLevelingCertPage(certDoc, input),
    input.sealWithRSA,
    input.keypair,
  );

  const stats = fs.statSync(input.outputPath);
  return {
    pdfPath: input.outputPath,
    pdfSizeBytes: stats.size,
    pageCount: 1 + input.pages.length + 1,
    sealed: sealResult.sealed,
    signatureFingerprint: sealResult.fingerprint,
    signedAt: sealResult.signedAt,
    warnings: [...warnings, ...sealResult.warnings],
  };
}

