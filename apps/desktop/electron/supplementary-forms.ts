/**
 * Supplementary Forms — RINEX Observation Log + Leveling Book
 *
 * Two supplementary statutory forms:
 *
 *   1. RINEX OBSERVATION LOG (Topographical)
 *      Per Survey of Kenya GNSS Practice Notes.
 *      Records GNSS observation sessions for post-processing:
 *        - Station info (marker, name, coords)
 *        - Receiver + antenna info
 *        - Session start/stop times (UTC)
 *        - Satellites observed
 *        - Ephemeris source
 *        - Weather conditions
 *        - Operator name
 *
 *   2. LEVELING BOOK (Engineering)
 *      Per Road Design Manual RDM 1.1 (2025), Section 5.
 *      Rise and fall method, page-validated:
 *        - Backsight (BS), foresight (FS) readings
 *        - Rise / fall computation
 *        - Reduced level (RL) computation
 *        - Page check: ∑BS - ∑FS = ∑rise - ∑fall = last RL - first RL
 *        - Closure: 10√K mm (where K = distance in km)
 *
 * All forms sealed with RSA-2048 via shared sealAndAppendCertificate helper.
 */

