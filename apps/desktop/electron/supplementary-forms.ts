export interface RinexSession {
  stationName: string;
  stationMarker: string;          // e.g. "TS1 - Concrete pillar"
  approxEasting: number;
  approxNorthing: number;
  approxElevation: number;        // m
  receiverMake: string;
  receiverModel: string;
  receiverSerial: string;
  receiverFirmware: string;
  antennaMake: string;
  antennaModel: string;
  antennaSerial: string;
  antennaHeight: number;          // m (vertical)
  heightMeasurementMethod: string; // e.g. "Slant to notch, corrected to vertical"
  sessionStartUTC: string;        // ISO datetime
  sessionEndUTC: string;
  sessionDuration: number;        // seconds
  observationInterval: number;    // seconds (e.g. 15, 30)
  satellitesObserved: string[];   // e.g. ["G01", "G07", "G15", ...]
  ephemerisSource: string;        // e.g. "Broadcast + precise"
  weather: {
    temperature: number;          // °C
    pressure: number;             // hPa
    humidity: number;             // %
    conditions: string;           // e.g. "Partly cloudy"
  };
  operatorName: string;
  notes?: string;
}

export interface RinexLogInput {
  project: CommonProject & {
    corsStations?: Array<{ name: string; distance: number; azimuth: string }>;
    processingCenter?: string;
  };
  surveyor: CommonSurveyor;
  sessions: RinexSession[];
  outputPath: string;
  sealWithRSA?: boolean;
  keypair?: SurveyorKeypair;
}

export async function generateRinexLog(input: RinexLogInput): Promise<FormResult> {
  log.info(`Generating RINEX Observation Log (${input.sessions.length} sessions)`);

  const warnings: string[] = [];
  if (input.sessions.length === 0) {
    warnings.push('No observation sessions provided.');
  }
  for (let i = 0; i < input.sessions.length; i++) {
    const s = input.sessions[i];
    if (s.sessionDuration < 1800) {
      warnings.push(`Session ${i + 1} (${s.stationName}): duration ${s.sessionDuration}s < 30min minimum for static GNSS.`);
    }
    if (s.satellitesObserved.length < 4) {
      warnings.push(`Session ${i + 1} (${s.stationName}): only ${s.satellitesObserved.length} satellites observed (<4 minimum).`);
    }
    if (s.antennaHeight <= 0) {
      warnings.push(`Session ${i + 1} (${s.stationName}): antenna height ${s.antennaHeight}m is invalid.`);
    }
  }

  const doc = new PDFDocument({
    size: [A4_WIDTH_PT, A4_HEIGHT_PT],
    margins: { top: MARGIN_PT, bottom: MARGIN_PT, left: MARGIN_PT, right: MARGIN_PT },
    info: {
      Title: `RINEX Observation Log — ${input.project.parcelNumber}`,
      Author: `${input.surveyor.name} (Lic. ${input.surveyor.license})`,
      Subject: 'GNSS Observation Sessions',
      Keywords: 'GNSS, RINEX, METARDU Desktop',
      Producer: 'METARDU Desktop',
    },
  });

  const stream = fs.createWriteStream(input.outputPath);
  doc.pipe(stream);

  // ─── Page 1: Header + Project Info + CORS Stations ────────────────
  drawHeader(doc, input.project, '1', 'RINEX OBSERVATION LOG');

  let y = MARGIN_PT + 30;
  doc.font('Helvetica-Bold').fontSize(FS_TITLE + 2).fillColor(COLOR_ACCENT)
    .text('RINEX OBSERVATION LOG', MARGIN_PT, y, { align: 'center', width: A4_WIDTH_PT - 2 * MARGIN_PT });
  y += 22;
  doc.font('Helvetica-Oblique').fontSize(FS_SUBHEADING).fillColor(COLOR_GREY)
    .text('Per Survey of Kenya GNSS Practice Notes 2020',
      MARGIN_PT, y, { align: 'center', width: A4_WIDTH_PT - 2 * MARGIN_PT });

  y += 22;
  const labelW = 50 * MM_TO_PT;
  const valW = (A4_WIDTH_PT - 2 * MARGIN_PT) - labelW;
  const projectRows: Array<[string, string]> = [
    ['Project Name', input.project.name],
    ['Parcel Number', input.project.parcelNumber],
    ['LR Number', input.project.lrNumber],
    ['County', input.project.county],
    ['Sub-County', input.project.subCounty ?? '—'],
    ['Locality', input.project.locality],
    ['Survey Date', input.project.surveyDate],
    ['Projection', input.project.projection],
    ['Datum', input.project.datum],
    ['Surveyor', `${input.surveyor.name} (Lic. ${input.surveyor.license})`],
    ['Surveyor Firm', input.surveyor.firmName ?? '—'],
    ['Processing Center', input.project.processingCenter ?? '—'],
    ['Total Sessions', String(input.sessions.length)],
  ];
  for (const [label, value] of projectRows) {
    doc.font('Helvetica-Bold').fontSize(FS_BODY).fillColor(COLOR_BLACK)
      .text(label, MARGIN_PT, y, { width: labelW });
    doc.font('Helvetica').fontSize(FS_BODY).fillColor(COLOR_BLACK)
      .text(value, MARGIN_PT + labelW, y, { width: valW });
    doc.moveTo(MARGIN_PT, y + 12).lineTo(A4_WIDTH_PT - MARGIN_PT, y + 12).lineWidth(0.2).stroke(COLOR_LIGHT_GREY);
    y += 14;
  }

  // CORS stations
  if (input.project.corsStations && input.project.corsStations.length > 0) {
    y += 10;
    doc.font('Helvetica-Bold').fontSize(FS_HEADING).fillColor(COLOR_ACCENT)
      .text('CORS Reference Stations', MARGIN_PT, y);
    y += 16;
    const cols = [
      { title: 'Station Name', x: MARGIN_PT, w: 60 * MM_TO_PT, align: 'left' as const },
      { title: 'Distance (km)', x: MARGIN_PT + 60 * MM_TO_PT, w: 40 * MM_TO_PT, align: 'right' as const },
      { title: 'Azimuth', x: MARGIN_PT + 100 * MM_TO_PT, w: 40 * MM_TO_PT, align: 'left' as const },
    ];
    for (const c of cols) {
      doc.font('Helvetica-Bold').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(c.title, c.x, y, { width: c.w, align: c.align });
    }
    y += 12;
    doc.moveTo(MARGIN_PT, y).lineTo(A4_WIDTH_PT - MARGIN_PT, y).lineWidth(0.5).stroke(COLOR_BLACK);
    for (const cors of input.project.corsStations) {
      y += 2;
      doc.font('Helvetica-Bold').fontSize(FS_SMALL).fillColor(COLOR_BLACK)
        .text(cors.name, cols[0].x, y, { width: cols[0].w, align: 'left' });
      doc.font('Courier').fontSize(FS_SMALL).fillColor(COLOR_BLACK)
        .text(cors.distance.toFixed(1), cols[1].x, y, { width: cols[1].w, align: 'right' });
      doc.font('Helvetica').fontSize(FS_SMALL).fillColor(COLOR_BLACK)
        .text(cors.azimuth, cols[2].x, y, { width: cols[2].w, align: 'left' });
      y += 12;
      doc.moveTo(MARGIN_PT, y).lineTo(A4_WIDTH_PT - MARGIN_PT, y).lineWidth(0.1).stroke(COLOR_LIGHT_GREY);
    }
  }

  drawFooter(doc, input.project, input.surveyor);

  // ─── Pages 2+: One page per session ──────────────────────────────
  for (let i = 0; i < input.sessions.length; i++) {
    const s = input.sessions[i];
    doc.addPage();
    drawHeader(doc, input.project, String(i + 2), `SESSION ${i + 1} — ${s.stationName}`);
    y = MARGIN_PT + 30;

    doc.font('Helvetica-Bold').fontSize(FS_TITLE).fillColor(COLOR_ACCENT)
      .text(`SESSION ${i + 1} — ${s.stationName}`, MARGIN_PT, y, { align: 'center', width: A4_WIDTH_PT - 2 * MARGIN_PT });
    y += 22;

    // Station info
    doc.font('Helvetica-Bold').fontSize(FS_HEADING).fillColor(COLOR_ACCENT)
      .text('Station Information', MARGIN_PT, y);
    y += 16;

    const stationRows: Array<[string, string]> = [
      ['Station Name', s.stationName],
      ['Marker Type', s.stationMarker],
      ['Approx. Easting', fmtCoord(s.approxEasting) + ' m'],
      ['Approx. Northing', fmtCoord(s.approxEasting) + ' m'],
      ['Approx. Elevation', s.approxElevation.toFixed(3) + ' m'],
    ];
    for (const [label, value] of stationRows) {
      doc.font('Helvetica-Bold').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(label, MARGIN_PT, y, { width: labelW });
      doc.font('Helvetica').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(value, MARGIN_PT + labelW, y, { width: valW });
      doc.moveTo(MARGIN_PT, y + 12).lineTo(A4_WIDTH_PT - MARGIN_PT, y + 12).lineWidth(0.2).stroke(COLOR_LIGHT_GREY);
      y += 14;
    }

    // Receiver info
    y += 8;
    doc.font('Helvetica-Bold').fontSize(FS_HEADING).fillColor(COLOR_ACCENT)
      .text('Receiver Information', MARGIN_PT, y);
    y += 16;

    const receiverRows: Array<[string, string]> = [
      ['Make', s.receiverMake],
      ['Model', s.receiverModel],
      ['Serial Number', s.receiverSerial],
      ['Firmware', s.receiverFirmware],
    ];
    for (const [label, value] of receiverRows) {
      doc.font('Helvetica-Bold').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(label, MARGIN_PT, y, { width: labelW });
      doc.font('Helvetica').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(value, MARGIN_PT + labelW, y, { width: valW });
      doc.moveTo(MARGIN_PT, y + 12).lineTo(A4_WIDTH_PT - MARGIN_PT, y + 12).lineWidth(0.2).stroke(COLOR_LIGHT_GREY);
      y += 14;
    }

    // Antenna info
    y += 8;
    doc.font('Helvetica-Bold').fontSize(FS_HEADING).fillColor(COLOR_ACCENT)
      .text('Antenna Information', MARGIN_PT, y);
    y += 16;

    const antennaRows: Array<[string, string]> = [
      ['Make', s.antennaMake],
      ['Model', s.antennaModel],
      ['Serial Number', s.antennaSerial],
      ['Antenna Height', s.antennaHeight.toFixed(3) + ' m'],
      ['Measurement Method', s.heightMeasurementMethod],
    ];
    for (const [label, value] of antennaRows) {
      doc.font('Helvetica-Bold').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(label, MARGIN_PT, y, { width: labelW });
      doc.font('Helvetica').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(value, MARGIN_PT + labelW, y, { width: valW });
      doc.moveTo(MARGIN_PT, y + 12).lineTo(A4_WIDTH_PT - MARGIN_PT, y + 12).lineWidth(0.2).stroke(COLOR_LIGHT_GREY);
      y += 14;
    }

    // Session info
    y += 8;
    doc.font('Helvetica-Bold').fontSize(FS_HEADING).fillColor(COLOR_ACCENT)
      .text('Session Information', MARGIN_PT, y);
    y += 16;

    const sessionRows: Array<[string, string]> = [
      ['Session Start (UTC)', s.sessionStartUTC],
      ['Session End (UTC)', s.sessionEndUTC],
      ['Duration', `${Math.floor(s.sessionDuration / 60)} min ${s.sessionDuration % 60} sec`],
      ['Observation Interval', `${s.observationInterval} s`],
      ['Satellites Observed', `${s.satellitesObserved.length} (${s.satellitesObserved.join(', ')})`],
      ['Ephemeris Source', s.ephemerisSource],
      ['Operator', s.operatorName],
    ];
    for (const [label, value] of sessionRows) {
      doc.font('Helvetica-Bold').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(label, MARGIN_PT, y, { width: labelW });
      doc.font('Helvetica').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(value, MARGIN_PT + labelW, y, { width: valW });
      doc.moveTo(MARGIN_PT, y + 12).lineTo(A4_WIDTH_PT - MARGIN_PT, y + 12).lineWidth(0.2).stroke(COLOR_LIGHT_GREY);
      y += 14;
    }

    // Weather
    y += 8;
    doc.font('Helvetica-Bold').fontSize(FS_HEADING).fillColor(COLOR_ACCENT)
      .text('Weather Conditions', MARGIN_PT, y);
    y += 16;

    const weatherRows: Array<[string, string]> = [
      ['Temperature', `${s.weather.temperature.toFixed(1)} °C`],
      ['Pressure', `${s.weather.pressure.toFixed(1)} hPa`],
      ['Humidity', `${s.weather.humidity.toFixed(0)} %`],
      ['Conditions', s.weather.conditions],
    ];
    for (const [label, value] of weatherRows) {
      doc.font('Helvetica-Bold').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(label, MARGIN_PT, y, { width: labelW });
      doc.font('Helvetica').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(value, MARGIN_PT + labelW, y, { width: valW });
      doc.moveTo(MARGIN_PT, y + 12).lineTo(A4_WIDTH_PT - MARGIN_PT, y + 12).lineWidth(0.2).stroke(COLOR_LIGHT_GREY);
      y += 14;
    }

    // Notes
    if (s.notes) {
      y += 8;
      doc.font('Helvetica-Bold').fontSize(FS_HEADING).fillColor(COLOR_ACCENT)
        .text('Notes', MARGIN_PT, y);
      y += 14;
      doc.font('Helvetica-Oblique').fontSize(FS_BODY).fillColor(COLOR_BLACK)
        .text(s.notes, MARGIN_PT, y, { width: A4_WIDTH_PT - 2 * MARGIN_PT });
    }

    drawFooter(doc, input.project, input.surveyor);
  }

  doc.end();
  await new Promise<void>((resolve, reject) => {
    stream.on('finish', () => resolve());
    stream.on('error', reject);
  });

  const sealResult = await sealAndAppendCertificate(
    input.outputPath,
    input.surveyor,
    input.project,
    (certDoc, _seal) => drawRinexCertPage(certDoc, input),
    input.sealWithRSA,
    input.keypair,
  );

  const stats = fs.statSync(input.outputPath);
  return {
    pdfPath: input.outputPath,
    pdfSizeBytes: stats.size,
    pageCount: 1 + input.sessions.length + 1, // header + sessions + cert
    sealed: sealResult.sealed,
    signatureFingerprint: sealResult.fingerprint,
    signedAt: sealResult.signedAt,
    warnings: [...warnings, ...sealResult.warnings],
  };
}

function drawRinexCertPage(doc: PDFKit.PDFDocument, input: RinexLogInput) {
  const yStart = MARGIN_PT + 30;
  doc.font('Helvetica-Bold').fontSize(FS_TITLE + 2).fillColor(COLOR_ACCENT)
    .text("SURVEYOR'S CERTIFICATE — GNSS OBSERVATIONS", MARGIN_PT, yStart, { align: 'center', width: A4_WIDTH_PT - 2 * MARGIN_PT });

  doc.font('Helvetica-Oblique').fontSize(FS_SUBHEADING).fillColor(COLOR_GREY)
    .text('Survey Regulations 1994, Regulation 3(2)',
      MARGIN_PT, yStart + 22, { align: 'center', width: A4_WIDTH_PT - 2 * MARGIN_PT });

  let y = yStart + 50;
  const totalDuration = input.sessions.reduce((s, sess) => s + sess.sessionDuration, 0);
  const body = [
    `I, ${input.surveyor.name} (Licensed Surveyor No. ${input.surveyor.license}),`,
    `hereby certify that the GNSS observations described in this log were`,
    `conducted by me (or under my immediate personal supervision) on`,
    `${input.project.surveyDate}, in accordance with the Survey Act (Cap. 299)`,
    `and the Survey Regulations 1994.`,
    '',
    `Observation particulars:`,
    `  Project:           ${input.project.name}`,
    `  Locality:          ${input.project.locality}, ${input.project.county} County`,
    `  Total sessions:    ${input.sessions.length}`,
    `  Total duration:    ${Math.floor(totalDuration / 3600)}h ${Math.floor((totalDuration % 3600) / 60)}m`,
    `  Stations observed: ${input.sessions.map(s => s.stationName).join(', ')}`,
    `  Processing center: ${input.project.processingCenter ?? '—'}`,
    '',
    `I further certify that:`,
    `  (a) All equipment was calibrated and functioning correctly;`,
    `  (b) Antenna heights were measured and recorded before and after each session;`,
    `  (c) The observation log is complete and accurate to the best of my knowledge.`,
  ];
  for (const line of body) {
    doc.font('Helvetica').fontSize(FS_BODY).fillColor(COLOR_BLACK)
      .text(line, MARGIN_PT, y, { width: A4_WIDTH_PT - 2 * MARGIN_PT });
    y += 13;
  }
}

